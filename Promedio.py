#Solicitar notas a usuario
PrimeraNota=float(input("Ingrese la primera nota: "))
SegundaNota=float(input("Ingrese la segunda nota: "))
TerceraNota=float(input("Ingrese la tercera nota: "))
CuartaNota=float(input("Ingrese la cuarta nota: "))

#Operacion de Promedio
Promedio= (PrimeraNota+SegundaNota+TerceraNota+CuartaNota)/4

#Mostrar Promedio de notas 
print(f"El promedio de sus notas es: {Promedio}")
